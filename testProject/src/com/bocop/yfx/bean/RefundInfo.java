package com.bocop.yfx.bean;

/**
 * 
 * 还款
 * 
 * @author lh
 * 
 */
public class RefundInfo {

	private String date;
	private String sum;
	private int icon;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getSum() {
		return sum;
	}

	public void setSum(String sum) {
		this.sum = sum;
	}

	public int getIcon() {
		return icon;
	}

	public void setIcon(int icon) {
		this.icon = icon;
	}

}
